# ============================================================================
# digit-recognizer 包
# ============================================================================
#
# Kaggle Digit Recognizer 竞赛的模块化实现
# 任务：识别 28x28 手写数字图片（0~9，10 分类）
#
# 包结构：
#   config.py     → 全局配置：超参数、路径、设备选择
#   dataset.py    → 数据集：从 CSV 加载，像素归一化
#   model.py      → 模型：3 层 MLP（784 → 256 → 128 → 10）
#   checkpoint.py → 早停机制 + 最佳模型自动保存
#   metrics.py    → 评估指标：准确率
#   trainer.py    → 训练循环：CrossEntropyLoss + Adam
#   main.py       → 主程序入口
#
# 使用方式：
#   python digit-recognizer/main.py
#
# 依赖库：
#   torch, numpy, pandas
#
# ============================================================================
