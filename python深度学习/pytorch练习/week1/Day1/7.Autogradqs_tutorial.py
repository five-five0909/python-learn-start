import torch

# ============================================================================
# 1. 张量、函数与计算图
# ============================================================================
# 创建一个最简单的单层神经网络，包含输入 x、参数 w 和 b，以及损失函数。
# 在这个过程中，PyTorch 会自动为我们构建一个计算图。

# 定义输入张量 x 和期望输出 y
x = torch.ones(5)    # 输入张量，形状为 (5,)
y = torch.zeros(3)   # 期望输出，形状为 (3,)

# 定义可学习的参数 w 和 b
# requires_grad=True 告诉 PyTorch 需要跟踪这些张量的所有操作，以便后续计算梯度
w = torch.randn(5, 3, requires_grad=True)  # 权重，形状 (5, 3)，每个元素随机来自标准正态分布
b = torch.randn(3, requires_grad=True)     # 偏置，形状 (3,)

# 前向传播：z = x * w + b，然后计算损失
z = torch.matmul(x, w) + b                 # 矩阵乘法: (5,) @ (5,3) -> (3,)
# 计算二元交叉熵损失（结合了 Sigmoid 激活和损失计算）。计算目标与输入Logit之间的二元交叉熵。
loss = torch.nn.functional.binary_cross_entropy_with_logits(z, y)

# ---------------------------------------------------------------------------
# grad_fn 属性
# pytorch 为每个参与了梯度追踪的张量都存储了一个 grad_fn 属性，
# 该属性引用了一个创建该张量的 Function 对象，它记录了反向传播时计算导数的函数。
# ---------------------------------------------------------------------------
print(f"Gradient function for z = {z.grad_fn}")
# 输出示例: <AddBackward0 object at 0x...>
# AddBackward0 表示 z 是由加法操作产生的，反向传播时会执行加法的梯度传播。

print(f"Gradient function for loss = {loss.grad_fn}")
# 输出示例: <BinaryCrossEntropyWithLogitsBackward0 object at 0x...>
# loss 由 binary_cross_entropy_with_logits 函数产生。


# ============================================================================
# 2. 计算梯度
# ============================================================================
# 为了优化参数 w 和 b，我们需要计算损失函数 loss 关于 w 和 b 的梯度。
# 调用 .backward() 方法会沿着计算图反向传播，并累加梯度到叶子张量的 .grad 属性中。

loss.backward()

# 查看计算得到的梯度
print(w.grad)   # 损失函数 loss 关于权重 w 的梯度，形状与 w 相同 (5,3)
print(b.grad)   # 损失函数 loss 关于偏置 b 的梯度，形状与 b 相同 (3,)

# 重要提示：
# 1. 只有计算图的叶子节点（即 requires_grad=True 且直接由用户创建的张量）才会有 .grad 属性。
# 2. 默认情况下，.backward() 只能调用一次。如果需要对同一个计算图多次调用，
#    需要设置 retain_graph=True，否则计算图会被释放以节省内存。


# ============================================================================
# 3. 禁用梯度跟踪
# ============================================================================
# 在某些情况下，我们不需要梯度计算（例如模型评估或纯前向推理时）。
# 可以通过 torch.no_grad() 上下文管理器临时关闭自动求导，节省计算和内存。
with torch.no_grad():
    # 在这个块内，所有计算都不会被记录到计算图中
    z_no_grad = torch.matmul(x, w) + b
    print(z_no_grad.requires_grad)  # False，说明该张量不需要梯度

# 达到同样效果的另一种方法是使用 .detach() 方法：
z_detached = z.detach()  # 返回一个与 z 共享数据，但脱离计算图的新张量
print(z_detached.requires_grad)  # False

# 通常禁用梯度跟踪的场景：
# - 将模型中的某些参数标记为 "冻结参数" (frozen parameters)，只微调其他部分。
# - 模型推理（评估/测试）时加速计算，因为不跟踪梯度的张量运算效率更高。


# ============================================================================
# 4. 更多关于计算图
# ============================================================================
# (概念性说明)
# 1. autograd 在一个称为“有向无环图 (DAG)”的数据结构中记录所有操作和数据。
#    叶子节点是输入张量，根节点是输出张量。
# 2. 前向传播时，autograd 同时做两件事：
#    - 执行请求的操作，计算结果张量。
#    - 在 DAG 中维护每个操作的梯度函数 (grad_fn)。
# 3. 当在 DAG 的根节点上调用 .backward() 时，反向传播开始：
#    - autograd 从每个 .grad_fn 计算梯度，
#    - 在相应张量的 .grad 属性中累积梯度，
#    - 使用链式法则将梯度一直传播到叶子张量。
# 4. PyTorch 中的 DAG 是动态的：每次调用 .backward() 后，计算图会重新创建。
#    这正是你可以在每次迭代中改变模型结构、形状或大小而不影响训练的原因。